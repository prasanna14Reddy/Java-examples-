import java.util.Scanner;
public class Example2 {

	public static void main(String[] args) {
      int a=5;//assignment
      a+=5;//a=a+5
      System.out.println(a);//a=10
      a-=5;//a=a-5
      System.out.println(a);//a=5
      a*=5;//a=a*5
      System.out.println(a);//a=25
      a/=5;//a=a/5
      System.out.println(a);//a=5
      a%=5;//a=a%5
      System.out.println(a);//a=0
	}

}
